"use client";

import Link from "next/link";
import { usePathname } from "next/navigation";

const NAV_ITEMS = [
  { href: "/dashboard", label: "Home", icon: HomeIcon },
  { href: "/history", label: "History", icon: HistoryIcon },
  { href: "/budgets", label: "Budgets", icon: BudgetsIcon },
  { href: "/settings", label: "Settings", icon: SettingsIcon },
] as const;

export function BottomNav() {
  const pathname = usePathname();

  return (
    <nav
      className="fixed bottom-0 left-0 right-0 z-30 flex items-center justify-around border-t border-white/10 bg-zinc-900/95 px-2 py-2 backdrop-blur-md md:hidden"
      aria-label="Bottom navigation"
    >
      {NAV_ITEMS.map(({ href, label, icon: Icon }) => {
        const isActive = pathname === href || (href !== "/dashboard" && pathname.startsWith(href));
        return (
          <Link
            key={href}
            href={href}
            className={`flex flex-col items-center gap-1 rounded-xl px-4 py-2 transition-colors ${
              isActive ? "text-[#2E8B57]" : "text-zinc-500 hover:text-zinc-300"
            }`}
          >
            <Icon className={`h-6 w-6 ${isActive ? "opacity-100" : "opacity-70"}`} />
            <span className="text-xs font-medium">{label}</span>
          </Link>
        );
      })}
    </nav>
  );
}

